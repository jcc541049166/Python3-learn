#!/usr/bin/env python3
# -*- coding:utf-8 -*-

# int class
def i(x):
    x = 100
    print (x)
i1 = 1
i(i1)
print (i1)

# str class
def s(x):
    x = x.upper()
    print (x)
s1 = "hello,neo"
s(s1)
print (s1)

# list class
def l(x):
    x[0] = 100
    print (x)
l1 = [3,2,1]
l(l1)
print (l1)

# tuple class
def t(x):
    x = (1,2,3)
    print (x)
t1 = ('a','b','c')
t(t1)
print (t1)


def d(x):
    x = {'a':1,'b':2,'c':3}
    print (x)

